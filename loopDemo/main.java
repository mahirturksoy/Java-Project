package loopDemo;

public class main {

    public static void main(String[] args) {
        
        //for

        for(int i = 0; i<10; i++)
        {
            System.out.println(i);
        }
        System.out.println("For dongusu bitti");


        int i = 0;
        //While
        while(i<10)
        {
            System.out.println(i);
            i++;
        }
        System.out.println("While dongusu bitti");

        //do while
        
        int j = 0;
        do{
            System.out.println(i);
            j++;

        } while(j<10);
        System.out.println("DoWhile dongusu bitti");

    }   
}